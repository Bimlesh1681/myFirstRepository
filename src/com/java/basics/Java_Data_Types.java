package com.java.basics;

public class Java_Data_Types {

	public static void main(String[] args) {
		
		//primitive data types
		
		//byte - 1 byte
		//short - 2 byte
		//int - 4 byte
		//long - 8 byte
		
		//float - 4 byte
		//double - 8 byte
		
		//boolean - 1 bit
		//char - 2 byte
		
		byte myByte = 10;
		System.out.println("myByte value is: "+myByte);
		
		float myFloat = 10.10f;
		System.out.println("myFloat value is: "+myFloat);
		
		double myDouble = 100.50;
		System.out.println("myDouble value is: "+myDouble);
		
		char myChar = 'A';
		char myChar1 = '6';
		System.out.println("myChar value is: "+myChar);
		System.out.println("myChar1 value is: "+myChar1);
		

	}

}
